    private void jTreeValueChanged(javax.swing.event.TreeSelectionEvent evt) {//GEN-FIRST:event_jTreeValueChanged
        ArtikelNode node = (ArtikelNode)
                           jTree.getLastSelectedPathComponent();
  
        // Wenn nichts gewählt zurück
        if (node == null) return;
       // Objekt holen wenn MittePanel == ArtikelPanel um die Auswahl
       // darzustellen
        if(hauptfenster.getMittePanel() instanceof ArtikelPanel){
           ArtikelPanel aPanel = (ArtikelPanel) hauptfenster.getMittePanel();
           aPanel.setArtikel(node.getID());
        }
        else
            return;
        
    }//GEN-LAST:event_jTreeValueChanged


