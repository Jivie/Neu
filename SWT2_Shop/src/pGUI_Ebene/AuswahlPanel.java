    public void addArtikel(pFach_Ebene.Artikel a) {
        DefaultMutableTreeNode root = (DefaultMutableTreeNode)jTree.getModel().getRoot();
        DefaultMutableTreeNode      parent;
        ArtikelNode neu = (ArtikelNode) root.getNextNode();
        while(neu != null){
            if(neu.getName().equals(a.getKategorie()) && neu.getID() == -1){
                neu.add(new ArtikelNode(a.getID(), a.getName()));
                return;
            }
            neu.getNextNode();
        }
        parent = new ArtikelNode(a.getKategorie());
        parent.add(new ArtikelNode(a.getID(), a.getName()));
        root.add(parent);
        jTree.updateUI();
    }
